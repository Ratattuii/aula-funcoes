// Define uma função chamada minhaFuncao que recebe um parâmetro chamado variavel
function minhaFuncao(variavel) {
    // Retorna o valor de variavel multiplicado por 5
    return variavel * 5;
  }
  
  // Chama a função minhaFuncao com o argumento 2 e imprime o resultado no console
  console.log(minhaFuncao(2)); // Isso imprimirá "10" no console
  
  // Chama a função minhaFuncao com o argumento 10 e imprime o resultado no console
  console.log(minhaFuncao(10)); // Isso imprimirá "50" no console
  